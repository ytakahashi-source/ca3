function renderSummary(){
  const card = document.getElementById('sum-card');
  if(!card) return;
  let html = '', totalDone=0, totalItems=0, worst=null;
  const rows = PHASES.map(p=>{
    const chk = state.checks[p.id] || {};
    const done = Object.values(chk).filter(Boolean).length;
    const total = p.checks.length;
    totalDone += done; totalItems += total;
    const pv = Math.round(done/total*100);
    const r = state.ratings[p.id];
    const rLabel = r!==undefined ? RATE_LABELS.find(x=>+x[0]===r)[1] : null;
    if(worst===null || pv < worst.pv) worst = {p, pv};
    const cls = pv<40?'low':pv<80?'mid':'';
    return `<div class="sum-row"><div class="sum-name">${p.name.replace(' 🎉GOAL','')}${rLabel?`<span class="sum-rate">${rLabel}</span>`:''}</div>
      <div class="sum-bar-wrap"><div class="sum-bar ${cls}" style="width:${pv}%"></div></div>
      <div class="sum-pct mono">${done}/${total}</div></div>`;
  }).join('');
  const avg = totalItems? Math.round(totalDone/totalItems*100) : 0;
  html = `<div class="sum-row" style="margin-bottom:14px"><div class="sum-name" style="font-size:15px">総合達成</div>
    <div class="sum-bar-wrap" style="height:20px"><div class="sum-bar" style="width:${avg}%;background:var(--orange)"></div></div>
    <div class="sum-pct mono" style="font-size:18px">${avg}%</div></div>` + rows
    + `<p style="font-size:12px;color:var(--ink-soft);margin-top:8px">⑤CPロードマップのチェック項目と連動（${totalDone}/${totalItems}項目 達成）。フェーズの4段階評価を付けた場合はフェーズ名の横に表示されます。</p>`;
  card.innerHTML = html;
  const wk = document.getElementById('weakest');
  if(worst && worst.pv < 100 && totalDone > 0){
    wk.style.display='block';
    wk.innerHTML = `<b>今週の重点フェーズ：${worst.p.name.replace(' 🎉GOAL','')}</b>（達成 ${worst.pv}%）<br>改善アクションは<b>1つに絞る</b>。⑤の該当フェーズを開き、未チェック項目から1つ選んでアクサポ・1on1でレビューする。`;
  } else { wk.style.display='none'; }
}

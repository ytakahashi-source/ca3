function renderJob(){
  renderJobNav();
  renderJobBody();
  document.getElementById('job-body').addEventListener('click',e=>{
    const h=e.target.closest('.job-head');
    if(h){ const c=h.parentElement; c.classList.toggle('open'); h.setAttribute('aria-expanded',c.classList.contains('open')); }
  });
}


let gloCat = 'all';
function renderGloNav(){
  const nav = document.getElementById('glo-nav');
  if(!nav) return;
  const cats = [{key:'all',ic:'',name:'すべて'}].concat(GLO_CATS);
  nav.innerHTML = '';
  cats.forEach(g=>{
    const b = document.createElement('button');
    b.className = 'glo-cat-btn' + (g.key===gloCat?' on':'');
    b.textContent = (g.ic?g.ic+' ':'') + g.name;
    b.addEventListener('click', ()=>{ gloCat=g.key; renderGloNav(); renderGloList(); });
    nav.appendChild(b);
  });
}
function renderGloList(){
  const list = document.getElementById('glo-list');
  if(!list) return;
  const q = (document.getElementById('glo-search').value||'').trim().toLowerCase();
  let items = GLOSSARY.filter(t=>gloCat==='all'||t.c===gloCat);
  if(q) items = items.filter(t=>(t.t+t.y+t.m).toLowerCase().includes(q));
  if(!items.length){
    list.innerHTML = '<p class="glo-empty">該当する用語が見つかりません。別のキーワードで検索してください。</p>';
    return;
  }
  const catName = k => { const c=GLO_CATS.find(x=>x.key===k); return c?c.name:''; };
  list.innerHTML = items.map(t=>`
    <div class="glo-item">
      <div class="glo-head-row">
        <span class="glo-term">${t.t}</span>
        ${t.y?`<span class="glo-yomi">${t.y}</span>`:''}
        <span class="glo-cat-tag">${catName(t.c)}</span>
      </div>
      <p class="glo-mean">${t.m}</p>
    </div>`).join('');
  const cnt = document.getElementById('glo-count');
  if(cnt) cnt.textContent = items.length + '語';
}
function renderGlossary(){
  renderGloNav();
  renderGloList();
  const s = document.getElementById('glo-search');
  if(s) s.addEventListener('input', renderGloList);
}


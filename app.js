function fmt(n){ return n>=100? Math.ceil(n).toLocaleString() : (Math.ceil(n*10)/10).toLocaleString(); }
function pct(n){ return (Math.round(n*10)/10).toLocaleString(); }
function prod(arr){ return arr.reduce((a,s)=>a*s.rate/100,1); }
/* 内訳の積 ＝ メイン率 を常に保つ（初期化時にメインを内訳から導出） */
function syncHeadFromSub(){
  HEAD.settei = +(prod(SUBA)*100).toFixed(1);
  HEAD.naitei = +(prod(SUBB)*100).toFixed(1);
}
/* メイン率が編集されたら、内訳の各率を等比スケールして積を一致させる */
function scaleSubToHead(sub, targetPct){
  const cur = prod(sub)*100;
  if(cur<=0) return;
  const factor = Math.pow((targetPct/100)/(cur/100), 1/sub.length);
  sub.forEach(s=>{ s.rate = Math.min(100, Math.max(1, +(s.rate*factor).toFixed(1))); });
}

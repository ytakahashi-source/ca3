let talkFilter = 'all';
function renderTalkNav(){
  const nav = document.getElementById('talk-nav');
  if(!nav) return;
  const groups = [{key:'all',ic:'',name:'すべて'}].concat(
    TALK_GROUPS.filter(g=>TALKS.some(t=>t.phase===g.key)));
  nav.innerHTML='';
  groups.forEach(g=>{
    const b=document.createElement('button');
    b.className='talk-nav-btn'+(g.key===talkFilter?' on':'');
    b.textContent=(g.ic?g.ic+' ':'')+g.name;
    b.addEventListener('click',()=>{ talkFilter=g.key; renderTalkNav(); renderTalkBody(); });
    nav.appendChild(b);
  });
}
function renderTalkBody(){
  const wrap = document.getElementById('talks');
  wrap.innerHTML='';
  TALK_GROUPS.forEach(g=>{
    if(talkFilter!=='all' && talkFilter!==g.key) return;
    const items = TALKS.filter(t=>t.phase===g.key);
    if(!items.length) return;
    const h=document.createElement('div');
    h.className='talk-group-h';
    h.innerHTML=`<span class="tg-ic">${g.ic}</span>${g.name}<span class="tg-cnt">${items.length}シーン</span>`;
    wrap.appendChild(h);
    items.forEach(t=>{ wrap.insertAdjacentHTML('beforeend', talkCardHtml(t)); });
  });
}
function renderTalks(){
  renderTalkNav();
  renderTalkBody();
  const wrap = document.getElementById('talks');
  wrap.addEventListener('click', e=>{
    const h = e.target.closest('.t-head');
    if(h){ const c = h.parentElement; c.classList.toggle('open'); h.setAttribute('aria-expanded', c.classList.contains('open')); }
  });
}

